    // Get SVG icon by name
    getIcon(iconName) {
        return this.icons[iconName] || this.icons['default'];
    }

    // Get SVG icon by emoji (for easy replacement)
    getIconFromEmoji(emoji) {
        const iconName = this.emojiToSvg[emoji];
        return iconName ? this.getIcon(iconName) : this.icons['default'];
    }

    // Replace emoji with SVG in a string
    replaceEmojiWithSvg(text) {
        let result = text;
        for (const [emoji, iconName] of Object.entries(this.emojiToSvg)) {
            result = result.replace(new RegExp(emoji, 'g'), this.getIcon(iconName));
        }
        return result;
    }

    // Create icon element with proper styling
    createIconElement(iconName, className = 'svg-icon', size = '24px') {
        const div = document.createElement('div');
        div.className = className;
        div.innerHTML = this.getIcon(iconName);
        div.style.cssText = `
            display: inline-flex;
            align-items: center;
            justify-content: center;
            width: ${size};
            height: ${size};
            flex-shrink: 0;
        `;
        return div;
    }

    // Replace emoji in DOM elements
    replaceEmojiInElement(element) {
        if (!element) return;
        
        // Replace text content
        if (element.childNodes.length === 1 && element.childNodes[0].nodeType === Node.TEXT_NODE) {
            const text = element.textContent;
            const hasEmoji = Object.keys(this.emojiToSvg).some(emoji => text.includes(emoji));
            if (hasEmoji) {
                element.innerHTML = this.replaceEmojiWithSvg(text);
            }
        } else {
            // Recursively process child elements
            Array.from(element.children).forEach(child => {
                this.replaceEmojiInElement(child);
            });
        }
    }

    // Replace all emojis in the document
    replaceAllEmojis() {
        this.replaceEmojiInElement(document.body);
    }
